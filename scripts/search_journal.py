def parse_search_results(html_content: str):
    """
    解析搜索结果HTML

    Args:
        html_content: 搜索结果HTML内容

    Returns:
        解析后的期刊列表和分页信息
    """
    soup = BeautifulSoup(html_content, "html.parser")
    journals = []
    
    # 提取分页信息
    total_pages = 1
    total_records = 0
    page_info = soup.find('b', string=re.compile(r'搜索条件匹配'))
    if page_info:
        info_text = page_info.get_text(strip=True)
        # 提取总记录数
        records_match = re.search(r'(\d+)条记录', info_text)
        if records_match:
            total_records = int(records_match.group(1))
        # 提取总页数
        pages_match = re.search(r'共(\d+)页', info_text)
        if pages_match:
            total_pages = int(pages_match.group(1))

    # 查找搜索结果表格
    result_table = soup.select_one('table.table_yjfx')
    if not result_table:
        return {'journals': journals, 'total_pages': total_pages, 'total_records': total_records}

    rows = result_table.find_all('tr')[1:]  # 跳过表头
    for row in rows:
        tds = row.find_all('td')
        if len(tds) < 7:
            continue

        journal = {}
        
        # ISSN (第1列)
        journal['issn'] = tds[0].get_text(strip=True)
        
        # 期刊名称和链接 (第2列)
        name_link = tds[1].select_one('a')
        if name_link:
            journal['name'] = name_link.get_text(strip=True)
            href = name_link.get('href', '')
            if 'journalid=' in href:
                journal['journal_id'] = href.split('journalid=')[-1].split('&')[0]
        
        # 简称 (第2列中的灰色字体)
        shortname = tds[1].select_one('font[color="grey"]')
        if shortname:
            journal['shortname'] = shortname.get_text(strip=True)
        
        # 影响因子 (第3列)
        if_text = tds[2].get_text(strip=True)
        if_match = re.search(r'([\d.]+)', if_text)
        if if_match:
            journal['impact_factor'] = if_match.group(1)
        
        # IF、h-index、CiteScore (第4列)
        stats_text = tds[3].get_text(strip=True)
        if_match = re.search(r'IF:\s*([\d.]+)', stats_text)
        if if_match:
            journal['real_time_if'] = if_match.group(1)
        h_match = re.search(r'h-index:\s*(\d+)', stats_text)
        if h_match:
            journal['h_index'] = h_match.group(1)
        cite_match = re.search(r'CiteScore:\s*([\d.]+)', stats_text)
        if cite_match:
            journal['cite_score'] = cite_match.group(1)
        
        # 分区 (第5列)
        journal['partition'] = tds[4].get_text(strip=True)
        
        # 学科 (第6列)
        journal['field'] = tds[5].get_text(strip=True)
        
        # SCI收录类型 (第7列)
        journal['sci_type'] = tds[6].get_text(strip=True)

        if journal:
            journals.append(journal)

    return {
        'journals': journals,
        'total_pages': total_pages,
        'total_records': total_records
    }


